// includes() : Recebe um argumento de substring e verifica se o argumento de substring existe na string. includes() retorna um booleano. Se uma substring existir em uma string, ela retornará true, caso contrário, retornará false.

let newName = "Daniel sousa"
console.log(newName.includes('sousa')); // True
console.log(newName.includes('sous@')); // False

// Sempre retorna um booleano 